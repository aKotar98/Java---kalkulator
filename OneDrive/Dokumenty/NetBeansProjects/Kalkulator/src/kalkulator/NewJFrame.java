    public void operation()
    {
    switch(calculate)
    {
        case 1://dodawanie
            helper2 = helper + Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(helper2));
            break;
        case 2://odejmowanie
            helper2 = helper - Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(helper2));
            break;
        case 3://mnozenie
            helper2 = helper * Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(helper2));
            break;
        case 4://dzielenie + brak możliwości dzielenia przez 0
            double check = Double.parseDouble(jTextField1.getText());
            if(check == 0){
            jTextField1.setText("Nie można dzielić przez zero!");
            }else{
            helper2 = helper / check;
            jTextField1.setText(Double.toString(helper2));
            }
            break; 
    }
    }
    
